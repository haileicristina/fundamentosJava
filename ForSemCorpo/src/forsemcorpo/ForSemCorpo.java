package forsemcorpo;
public class ForSemCorpo {
    public static void main(String[] args) {
        int soma = 0;
        //inicialização, condição, atualização
        for(int i = 1; i<=5; soma+= i++){
            System.out.println("Incremento: " + i +" Valor soma=  "  + soma);
        }
    }
    
}
